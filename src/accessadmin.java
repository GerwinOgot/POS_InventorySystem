    @SuppressWarnings("unchecked")
    private void Fetch(){
        try {
            int a;
            pst = con.prepareStatement("SELECT * FROM users");
            rs = pst.executeQuery();
            ResultSetMetaData rss = rs.getMetaData();
            a = rss.getColumnCount();
            
            DefaultTableModel df = (DefaultTableModel)accesstable.getModel();
            df.setRowCount(0);
            while(rs.next()){
                Vector v2 = new Vector();
                for(int x=1; x<=a; x++){
                    v2.add(rs.getString("user_ID"));
                    v2.add(rs.getString("first_name"));
                    v2.add(rs.getString("last_name"));
                    v2.add(rs.getString("user_name"));
                    v2.add(rs.getString("password"));
                    v2.add(rs.getString("type_of_user"));
                    v2.add(rs.getString("level_of_access"));
                }
                df.addRow(v2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(inventoryadmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

