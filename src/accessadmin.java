    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        try {
            DefaultTableModel model = (DefaultTableModel) accesstable.getModel();
            if(accesstable.getSelectedRowCount()==0){
                JOptionPane.showMessageDialog(this, "User not selected");
            }
            else{
                int row = accesstable.getSelectedRow();
                String selected = (String) accesstable.getModel().getValueAt(row, 0);
                pst = con.prepareStatement("DELETE FROM users WHERE user_ID=?");
                pst.setString(1,selected);
                int k = pst.executeUpdate();
                if (k==1){
                    JOptionPane.showMessageDialog(this, "User deleted");
                    Fetch();
                }
                else{
                    JOptionPane.showMessageDialog(this, "User not deleted");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(accessadmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteActionPerformed

